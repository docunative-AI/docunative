def generate_mkqa_report(
    results: list[dict],
    output_path: Path,
) -> None:
    """
    Generate MKQA Eval 3 report.

    Shows F1, EM, Recall@3 per language with H2 gradient verdict.
    Compares against Eval 1/2 results if available.
    """
    from eval.metrics import per_language_breakdown, nli_label_distribution

    lines = []

    def log(msg: str = "") -> None:
        print(msg)
        lines.append(msg)

    W = 85

    log("=" * W)
    log(" DOCUNATIVE — EVAL 3: MKQA REAL-WORLD BENCHMARK")
    log(" Citation: Longpre et al. (2021), ACL Anthology 2021.tacl-1.82")
    log(f" Total results: {len(results)}")
    log("=" * W)
    log()
    log(" Method: MKQA questions paired with Wikipedia article summaries as documents.")
    log(" Ground truth: MKQA gold answers in Chinese, Hindi, and Polish.")
    log(" Purpose: Validate synthetic Eval 1/2 findings on real-world data.")
    log()

    models = sorted({r["model"] for r in results})

    for model in models:
        model_results = [r for r in results if r["model"] == model]

        log(f"MODEL: {model}  ({len(model_results)} QA pairs)")
        log("-" * W)

        breakdown = per_language_breakdown(model_results)

        log(
            f"{'Language':<12} | {'Resource':>14} | {'Avg F1':>8} | "
            f"{'Avg EM':>7} | {'Recall@3':>9} | {'Refusal%':>9} | "
            f"{'Entail%':>8} | {'N':>5}"
        )
        log("-" * W)

        resource = {
            "zh": "High (1.9%)",
            "hi": "Medium (1.7%)",
            "pl": "Medium-low (1.4%)",
        }

        for lang in ["zh", "hi", "pl"]:
            if lang not in breakdown:
                continue
            b = breakdown[lang]
            log(
                f"{lang:<12} | {resource.get(lang, '?'):>14} | {b['avg_f1']:>8.3f} | "
                f"{b.get('avg_em', 0):>7.3f} | {b['recall_at_3']:>9.3f} | "
                f"{b.get('refusal_rate', 0):>9.3f} | {b['entailment_percentage']:>8.3f} | "
                f"{b['total_questions']:>5}"
            )

        log()

        # H2 verdict on real-world data
        if all(l in breakdown for l in ["zh", "hi", "pl"]):
            zh_f1 = breakdown["zh"]["avg_f1"]
            hi_f1 = breakdown["hi"]["avg_f1"]
            pl_f1 = breakdown["pl"]["avg_f1"]

            log(" H2 VERDICT (Real-World MKQA):")
            if zh_f1 >= hi_f1 >= pl_f1:
                log(f"  ✅ CONFIRMED — zh ({zh_f1}) ≥ hi ({hi_f1}) ≥ pl ({pl_f1})")
                log("  Internal training proportion gradient predicts performance")
                log("  on REAL Wikipedia documents — result generalises beyond synthetic data.")
            else:
                log(f"  ❌ NOT CONFIRMED — zh ({zh_f1}), hi ({hi_f1}), pl ({pl_f1})")
                log("  Gradient not observed on real-world data.")
                log("  Possible explanation: Wikipedia text complexity differs from")
                log("  synthetic legal documents. Tiny Aya's balancing may be effective")
                log("  for encyclopaedic content even if not for legal document QA.")
            log()

        # Wikipedia document stats
        wiki_chars = [r["wiki_chars"] for r in model_results]
        if wiki_chars:
            avg_chars = round(sum(wiki_chars) / len(wiki_chars))
            log(f" Wikipedia summary stats: avg {avg_chars} chars per document")
            log(f" ({avg_chars // 4} approx tokens — real documents vs synthetic)")
            log()

    # ── Comparison with synthetic evals ──────────────────────────────────
    synthetic_path = RESULTS_DIR / "eval_results.jsonl"
    if synthetic_path.exists():
        log("=" * W)
        log(" COMPARISON: SYNTHETIC (Eval 1) vs REAL-WORLD (Eval 3 MKQA)")
        log("=" * W)

        synthetic_results = []
        with open(synthetic_path, encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line:
                    synthetic_results.append(json.loads(line))

        if synthetic_results:
            synth_bd = per_language_breakdown(synthetic_results)

            log(f"{'Language':<12} | {'Eval 1 F1 (Synthetic)':>22} | {'Eval 3 F1 (MKQA)':>18} | {'Delta':>7}")
            log("-" * W)

            for lang in ["zh", "hi", "pl"]:
                synth_f1 = synth_bd.get(lang, {}).get("avg_f1", None)
                mkqa_results_lang = [r for r in results if r["language"] == lang]
                mkqa_bd = per_language_breakdown(mkqa_results_lang)
                mkqa_f1 = mkqa_bd.get(lang, {}).get("avg_f1", None)

                if synth_f1 is not None and mkqa_f1 is not None:
                    delta = round(mkqa_f1 - synth_f1, 3)
                    delta_str = f"+{delta}" if delta >= 0 else str(delta)
                    log(f"{lang:<12} | {synth_f1:>22.3f} | {mkqa_f1:>18.3f} | {delta_str:>7}")
                else:
                    log(f"{lang:<12} | {'N/A':>22} | {'N/A':>18} | {'—':>7}")

            log()
            log(" Interpretation:")
            log("  Positive delta (+): Model performs better on real Wikipedia than synthetic docs")
            log("  Negative delta (-): Synthetic docs were easier than real-world Wikipedia")
            log("  Similar delta:      Findings generalise well across document types")

    log()
    log("=" * W)
    log(" CITATION")
    log("=" * W)
    log()
    log(" Longpre, S., Lu, Y., & Daiber, J. (2021).")
    log(" MKQA: A Linguistically Diverse Benchmark for Multilingual Open Domain")
    log(" Question Answering. Transactions of the Association for Computational")
    log(" Linguistics, 9, 1389-1406. https://aclanthology.org/2021.tacl-1.82/")
    log()
    log("=" * W)

    # Save
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"\nMKQA report saved to {output_path}")


