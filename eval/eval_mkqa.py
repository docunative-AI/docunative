"""
eval/eval_mkqa.py
-----------------
Eval 3 — Real-World External Validation using MKQA.

MKQA (Multilingual Knowledge Questions and Answers) is a linguistically diverse
benchmark for multilingual open-domain QA (Longpre et al., 2021).
Citation: https://aclanthology.org/2021.tacl-1.82/

WHY MKQA FOR EVAL 3:
DocuNative's synthetic Eval 1 and Eval 2 use documents generated by Aya Expanse 32B.
A reviewer might argue: "Your synthetic documents are simple — the gradient you observe
is an artifact of document complexity, not language resource level."

MKQA answers this objection: it uses real Wikipedia content as the document source
and real multilingual questions/answers written by humans. If the zh > hi > pl gradient
holds on MKQA, it becomes an undeniable scientific finding.

THE CHALLENGE:
MKQA is open-domain QA (question + answer only). DocuNative is a RAG system — it needs
a document to read. Our solution: pair each MKQA question with its Wikipedia article
summary as the "document". The Wikipedia API provides this automatically from the
English query string.

PIPELINE:
  MKQA query (English) → Wikipedia API → article summary → DocuNative RAG
  MKQA question (zh/hi/pl) → DocuNative → answer
  MKQA answer (zh/hi/pl) → ground truth for F1, EM, Recall@3

SUPPORTED LANGUAGES:
  - zh (Chinese Simplified) — Internal 1.9%
  - hi (Hindi)              — Internal 1.7%
  - pl (Polish)             — Internal 1.4%

MKQA language keys:
  - Chinese: "zh_cn" in MKQA dataset
  - Hindi:   "hi"    in MKQA dataset
  - Polish:  "pl"    in MKQA dataset

Usage:
    # Quick test — 10 queries per language
    python -m eval.eval_mkqa --limit 10

    # Full run — up to 100 queries per language (recommended for demo)
    python -m eval.eval_mkqa --limit 100

    # Single language
    python -m eval.eval_mkqa --language zh --limit 50

    # Full 1000 queries (takes ~6-8 hours on Metal)
    python -m eval.eval_mkqa --limit 1000

    # Save results to custom path
    python -m eval.eval_mkqa --limit 100 --output eval/results/mkqa_report.txt

Author: Vinod Anbalagan
Citation: Longpre, S., Lu, Y., & Daiber, J. (2021). MKQA: A Linguistically Diverse
          Benchmark for Multilingual Open Domain Question Answering.
          Transactions of the Association for Computational Linguistics, 9, 1389-1406.
          https://aclanthology.org/2021.tacl-1.82/
"""

