# Single query evaluator
# ---------------------------------------------------------------------------

def run_single_mkqa_query(
    item: dict,
    language: str,
    model_choice: str,
    query_num: int,
    total: int,
) -> dict | None:
    """
    Run one MKQA query through the DocuNative pipeline for a specific language.

    Steps:
    1. Fetch Wikipedia summary as the "document"
    2. Write summary to temp .txt file
    3. Run DocuNative pipeline (embed → retrieve → generate → validate)
    4. Score against MKQA ground truth answer

    Args:
        item:         MKQA query dict from load_mkqa_queries()
        language:     DocuNative language code ("zh", "hi", "pl")
        model_choice: "Global" or "Fire"
        query_num:    Current query number (for logging)
        total:        Total queries (for logging)

    Returns:
        Result dict with all metrics, or None if Wikipedia fetch failed
    """
    from pipeline.pipeline import run
    from pipeline.validate import ParsedOutput
    from eval.metrics import (
        calculate_f1_score,
        calculate_exact_match,
        calculate_recall_3,
    )

    query_id  = item["query_id"]
    eng_query = item["query"]
    gt_answer = item["answers"][language]
    question  = item["questions"].get(language, eng_query)

    logger.info(
        "[%d/%d] query_id=%d | lang=%s | %s",
        query_num, total, query_id, language, eng_query[:60],
    )

    # Step 1: Fetch Wikipedia summary
    wiki_summary = _fetch_wikipedia_summary(eng_query)
    if wiki_summary is None:
        logger.warning("Skipping query_id=%d — no Wikipedia summary for %r", query_id, eng_query)
        return None

    # Step 2: Write to temp file
    tmp_path = Path(f"/tmp/mkqa_eval_{language}_{query_id}.txt")
    tmp_path.write_text(wiki_summary, encoding="utf-8")

    # Step 3: Run pipeline
    t0 = time.time()
    result = run(
        pdf_path=str(tmp_path),
        question=question,
        model_choice=model_choice,
        force_reindex=True,
    )
    elapsed = round(time.time() - t0, 2)

    if result.error:
        logger.warning("Pipeline error for query_id=%d: %s", query_id, result.error)
        return None

    # Step 4: Score
    parsed = ParsedOutput(
        answer=result.answer,
        source_quote=result.source_quote,
        parse_success=result.parse_success,
        raw_output="",
    )

    f1     = calculate_f1_score(parsed, gt_answer, lang=language)
    em     = calculate_exact_match(parsed, gt_answer, lang=language)
    recall = calculate_recall_3(result.retrieved_chunks, gt_answer, lang=language)

    return {
        # Identifiers
        "query_id":      query_id,
        "language":      language,
        "model":         model_choice,
        "eval_set":      "mkqa",
        # Content
        "query_en":      eng_query,
        "question":      question,
        "ground_truth":  gt_answer,
        "prediction":    result.answer,
        "source_quote":  result.source_quote,
        "wiki_chars":    len(wiki_summary),
        # Scores
        "f1_score":      f1,
        "em_score":      em,
        "recall_3":      recall,
        "nli_label":     result.nli_verdict,
        "parse_ok":      result.parse_success,
        "elapsed_s":     elapsed,
    }


# ---------------------------------------------------------------------------
# Main evaluation runner
# ---------------------------------------------------------------------------

def run_mkqa_evaluation(
    languages: list[str],
    model_choice: str = "Global",
    limit: int = 100,
) -> list[dict]:
    """
    Run MKQA Eval 3 for all specified languages.

    Each query is run once per language — same Wikipedia document, different
    question language and ground truth answer. This is a controlled comparison:
    document content is identical across languages for each query.

    Args:
        languages:    List of language codes to evaluate
        model_choice: "Global" or "Fire"
        limit:        Max queries per language

    Returns:
        Flat list of result dicts, one per (query, language) pair
    """
    # Load and filter MKQA queries
    queries = load_mkqa_queries(languages, limit=limit)

    if not queries:
        logger.error("No valid MKQA queries found. Check dataset and language settings.")
        return []

    all_results = []
    total = len(queries) * len(languages)
    n = 0

    for item in queries:
        for language in languages:
            n += 1
            result = run_single_mkqa_query(
                item=item,
                language=language,
                model_choice=model_choice,
                query_num=n,
                total=total,
            )
            if result:
                all_results.append(result)
            # Small delay to be polite to Wikipedia API (only on cache miss)
            time.sleep(0.1)

    logger.info("Completed MKQA eval: %d/%d results", len(all_results), total)
    return all_results


