def main() -> None:
    logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")

    parser = argparse.ArgumentParser(
        description=(
            "Eval 3 — MKQA real-world benchmark evaluation. "
            "Pairs MKQA questions with Wikipedia summaries as documents. "
            "Citation: Longpre et al. (2021), https://aclanthology.org/2021.tacl-1.82/"
        )
    )
    parser.add_argument(
        "--language",
        choices=["zh", "hi", "pl"],
        default=None,
        help="Evaluate a single language only. Default: all three (zh, hi, pl).",
    )
    parser.add_argument(
        "--model",
        choices=["Global", "Fire"],
        default="Global",
        help="Model to evaluate. Default: Global.",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=100,
        help="Maximum MKQA queries to evaluate per language. Default: 100.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=RESULTS_DIR / "eval_mkqa_report.txt",
        help="Where to save the report. Default: eval/results/eval_mkqa_report.txt",
    )
    args = parser.parse_args()

    languages = [args.language] if args.language else ["zh", "hi", "pl"]

    print(f"\nDocuNative Eval 3 — MKQA Real-World Benchmark")
    print(f"Languages:  {', '.join(languages)}")
    print(f"Model:      {args.model}")
    print(f"Limit:      {args.limit} queries per language")
    print(f"Output:     {args.output}")
    print(f"\nCitation: Longpre et al. (2021), ACL Anthology 2021.tacl-1.82")
    print(f"Dataset:  HuggingFace mkqa\n")

    # Check llama-server is running
    from pipeline.generate import check_server_health
    if not check_server_health():
        print("ERROR: llama-server is not running.")
        print("Start it first: make server-global")
        return

    # Run evaluation
    results = run_mkqa_evaluation(
        languages=languages,
        model_choice=args.model,
        limit=args.limit,
    )

    if not results:
        print("No results — check that MKQA loaded correctly and Wikipedia is accessible.")
        return

    # Save raw results
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    raw_path = RESULTS_DIR / "eval_mkqa_results.jsonl"
    with open(raw_path, "w", encoding="utf-8") as f:
        for r in results:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    print(f"\nRaw results saved to {raw_path}")

    # Generate report
    generate_mkqa_report(results, args.output)


if __name__ == "__main__":
    main()
