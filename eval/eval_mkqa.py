logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

RESULTS_DIR = Path("eval/results")

# MKQA language codes → DocuNative language codes
LANG_MAP = {
    "zh": "zh_cn",   # MKQA uses "zh_cn" for Chinese Simplified
    "hi": "hi",      # Same in MKQA
    "pl": "pl",      # Same in MKQA
}

# Human-readable resource level for report
RESOURCE_LEVEL = {
    "zh": "High (1.9%)",
    "hi": "Medium (1.7%)",
    "pl": "Medium-low (1.4%)",
}

# Wikipedia API endpoint — no auth required, generous rate limits
WIKIPEDIA_API = "https://en.wikipedia.org/api/rest_v1/page/summary/{title}"

# Minimum Wikipedia summary length to be useful as a document
MIN_SUMMARY_CHARS = 200

# Cache directory for Wikipedia summaries — avoid re-fetching
WIKI_CACHE_DIR = Path("eval/.wiki_cache")


# ---------------------------------------------------------------------------
# Wikipedia fetcher
# ---------------------------------------------------------------------------

def _fetch_wikipedia_summary(query: str, timeout: int = 10) -> str | None:
    """
    Fetch Wikipedia article summary for a query using the REST API.

    Uses the English Wikipedia because:
    1. MKQA queries are English-sourced questions about encyclopaedic facts
    2. English Wikipedia is the most complete and reliable source
    3. DocuNative's cross-lingual embeddings (BGE-M3) handle language mismatch

    The Wikipedia REST summary endpoint returns a plain-text extract of the
    article introduction — typically 1-4 paragraphs. This serves as our
    "document" for the RAG pipeline.

    Args:
        query:   The English query string from MKQA
        timeout: HTTP timeout in seconds

    Returns:
        Plain text summary string, or None if not found / too short
    """
    # Check cache first
    cache_key = query.lower().replace(" ", "_").replace("/", "_")[:100]
    cache_file = WIKI_CACHE_DIR / f"{cache_key}.txt"

    if cache_file.exists():
        summary = cache_file.read_text(encoding="utf-8")
        if len(summary) >= MIN_SUMMARY_CHARS:
            return summary
        return None

    # Fetch from Wikipedia API
    title = urllib.parse.quote(query.replace(" ", "_"))
    url = WIKIPEDIA_API.format(title=title)

    try:
        req = urllib.request.Request(
            url,
            headers={"User-Agent": "DocuNative-Eval/1.0 (research project)"},
        )
        with urllib.request.urlopen(req, timeout=timeout) as response:
            data = json.loads(response.read().decode("utf-8"))
            summary = data.get("extract", "")

        if len(summary) < MIN_SUMMARY_CHARS:
            logger.debug("Wikipedia summary too short for %r (%d chars)", query, len(summary))
            # Cache the empty result to avoid re-fetching
            WIKI_CACHE_DIR.mkdir(parents=True, exist_ok=True)
            cache_file.write_text("", encoding="utf-8")
            return None

        # Cache the summary
        WIKI_CACHE_DIR.mkdir(parents=True, exist_ok=True)
        cache_file.write_text(summary, encoding="utf-8")
        logger.debug("Fetched Wikipedia summary for %r (%d chars)", query, len(summary))
        return summary

    except Exception as e:
        logger.debug("Wikipedia fetch failed for %r: %s", query, e)
        return None


# ---------------------------------------------------------------------------
# MKQA dataset loader and filter
# ---------------------------------------------------------------------------

def load_mkqa_queries(
    languages: list[str],
    limit: int = 100,
) -> list[dict]:
    """
    Load and filter MKQA queries for the specified languages.

    Filters for queries where ALL requested languages have non-null text answers.
    This ensures fair comparison across languages on the same query set.

    MKQA dataset structure:
        {
            "query": "Who wrote Hamlet?",           ← English question
            "answers": {
                "zh_cn": [{"text": "莎士比亚"}],   ← Chinese answer
                "hi":    [{"text": "शेक्सपियर"}],  ← Hindi answer
                "pl":    [{"text": "Szekspir"}],    ← Polish answer
                ...
            }
        }

    Args:
        languages: List of DocuNative language codes ("zh", "hi", "pl")
        limit:     Maximum queries to return

    Returns:
        List of filtered query dicts with structure:
        {
            "query_id":    int,
            "query":       str,   ← English query for Wikipedia lookup
            "answers":     {lang: str},  ← ground truth per language
            "questions":   {lang: str},  ← translated questions (if available)
        }
    """
    # MKQA uses a legacy script-based loader that is no longer supported
    # by the datasets library. We download the raw JSONL directly from
    # the Apple GitHub repository instead.
    import urllib.request
    import json as _json

    mkqa_cache = Path("eval/.wiki_cache/mkqa_raw.jsonl")
    mkqa_cache.parent.mkdir(parents=True, exist_ok=True)

    if not mkqa_cache.exists():
        logger.info("Downloading MKQA dataset from GitHub (10k queries, ~5MB)...")
        url = "https://github.com/apple/ml-mkqa/raw/main/dataset/mkqa.jsonl.gz"
        gz_path = Path("eval/.wiki_cache/mkqa_raw.jsonl.gz")
        try:
            req = urllib.request.Request(
                url,
                headers={"User-Agent": "DocuNative-Eval/1.0"},
            )
            with urllib.request.urlopen(req, timeout=60) as response:
                gz_path.write_bytes(response.read())
            # Decompress
            import gzip
            with gzip.open(gz_path, "rb") as f_in:
                mkqa_cache.write_bytes(f_in.read())
            gz_path.unlink()
            logger.info("MKQA dataset downloaded and cached.")
        except Exception as e:
            raise RuntimeError(
                f"Failed to download MKQA dataset: {e}\n"
                "Please download mkqa.jsonl manually from "
                "https://github.com/apple/ml-mkqa and place it at "
                "eval/.wiki_cache/mkqa_raw.jsonl"
            )
    else:
        logger.info("Loading MKQA from cache: %s", mkqa_cache)

    # Load all records
    raw_records = []
    with open(mkqa_cache, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                raw_records.append(_json.loads(line))

    # Wrap in a simple list-of-dicts interface
    # Each record has: example_id, queries (dict), answers (dict)
    class _Dataset:
        def __init__(self, records):
            self._records = records
        def __len__(self):
            return len(self._records)
        def __iter__(self):
            return iter(self._records)

    dataset = _Dataset(raw_records)
    logger.info("Loaded %d MKQA queries total", len(dataset))

    # Map DocuNative lang codes to MKQA lang keys
    mkqa_keys = [LANG_MAP[lang] for lang in languages]

    valid_queries = []
    skipped_no_answer = 0
    skipped_null = 0

    for i, item in enumerate(dataset):
        # MKQA JSONL structure:
        # {"example_id": ..., "queries": {"en": "...", "zh_cn": "...", ...},
        #  "answers": {"en": [{"type": ..., "text": "..."}], "zh_cn": [...], ...}}
        answers = item.get("answers", {})
        queries_dict = item.get("queries", {})

        # English query for Wikipedia lookup
        query = queries_dict.get("en", item.get("query", "")).strip()
        if not query:
            skipped_no_answer += 1
            continue

        # Check all required languages have non-null text answers
        lang_answers = {}
        valid = True
        for doc_lang, mkqa_key in zip(languages, mkqa_keys):
            lang_ans = answers.get(mkqa_key, [])
            if not lang_ans:
                valid = False
                skipped_null += 1
                break
            # MKQA answers: list of {"type": ..., "text": ...}
            text_answers = [
                a.get("text", "") for a in lang_ans
                if a.get("text") and str(a.get("text", "")).strip()
            ]
            if not text_answers:
                valid = False
                skipped_null += 1
                break
            lang_answers[doc_lang] = str(text_answers[0]).strip()

        if not valid:
            continue

        # Get translated questions (MKQA has these under "queries" dict)
        questions = {}
        for doc_lang, mkqa_key in zip(languages, mkqa_keys):
            translated = queries_dict.get(mkqa_key, "").strip()
            questions[doc_lang] = translated if translated else query

        valid_queries.append({
            "query_id":  i,
            "query":     query,       # English — for Wikipedia lookup
            "answers":   lang_answers,
            "questions": questions,
        })

        if len(valid_queries) >= limit:
            break

    logger.info(
        "Filtered to %d valid queries (%d skipped — null answers, %d skipped — no query)",
        len(valid_queries), skipped_null, skipped_no_answer,
    )
    return valid_queries


